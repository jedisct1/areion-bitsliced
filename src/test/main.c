static uint8_t xtime(uint8_t x) { return (uint8_t)((x << 1) ^ ((x >> 7) * 0x1b)); }

static void aes_last(uint8_t x[16])
{
    uint8_t t[16];
    size_t column;
    size_t row;

    for (column = 0; column < 4; column++) {
        for (row = 0; row < 4; row++) {
            t[4 * column + row] = aes_sbox[x[4 * ((column + row) & 3) + row]];
        }
    }
    memcpy(x, t, sizeof t);
}

static void aes_round(uint8_t x[16])
{
    size_t column;

    aes_last(x);
    for (column = 0; column < 4; column++) {
        uint8_t* const a = x + 4 * column;
        const uint8_t sum = a[0] ^ a[1] ^ a[2] ^ a[3];
        const uint8_t a0 = a[0];

        a[0] ^= sum ^ xtime(a[0] ^ a[1]);
        a[1] ^= sum ^ xtime(a[1] ^ a[2]);
        a[2] ^= sum ^ xtime(a[2] ^ a[3]);
        a[3] ^= sum ^ xtime(a[3] ^ a0);
    }
}

static void xor16(uint8_t dst[16], const uint8_t src[16])
{
    size_t i;

    for (i = 0; i < 16; i++) {
        dst[i] ^= src[i];
    }
}

static void areion256_reference(uint8_t state[32])
{
    uint8_t* x[2] = { state, state + 16 };
    uint8_t t[16];
    size_t round;

    for (round = 0; round < 10; round++) {
        const size_t active = round & 1;
        const size_t target = active ^ 1;

        memcpy(t, x[active], sizeof t);
        aes_round(t);
        xor16(t, round_constants[round]);
        aes_round(t);
        xor16(t, x[target]);
        aes_last(x[active]);
        memcpy(x[target], t, sizeof t);
    }
}

static void areion512_round(
    uint8_t a[16], uint8_t b[16], uint8_t c[16], uint8_t d[16], const uint8_t rc[16])
{
    uint8_t t[16];

    memcpy(t, a, sizeof t);
    aes_round(t);
    xor16(b, t);
    memcpy(t, c, sizeof t);
    aes_round(t);
    xor16(d, t);
    aes_last(a);
    aes_last(c);
    xor16(c, rc);
    aes_round(c);
}

static void areion512_reference(uint8_t state[64])
{
    uint8_t* x[4] = { state, state + 16, state + 32, state + 48 };
    uint8_t t[16];
    size_t round;

    for (round = 0; round < 15; round++) {
        areion512_round(x[round & 3], x[(round + 1) & 3], x[(round + 2) & 3], x[(round + 3) & 3],
            round_constants[round]);
    }
    memcpy(t, x[0], sizeof t);
    memcpy(x[0], x[3], sizeof t);
    memcpy(x[3], x[2], sizeof t);
    memcpy(x[2], x[1], sizeof t);
    memcpy(x[1], t, sizeof t);
}

static void print_hex(const uint8_t* x, size_t len)
{
    size_t i;

    for (i = 0; i < len; i++) {
        fprintf(stderr, "%02x", x[i]);
    }
    fputc('\n', stderr);
}

typedef void (*permute_fn)(uint8_t*);

static int check_vector(const char* name, size_t len, permute_fn permute, permute_fn reference,
    int sequential, const uint8_t* expected)
{
    uint8_t state[64] = { 0 };
    uint8_t ref[64] = { 0 };
    size_t i;

    if (sequential) {
        for (i = 0; i < len; i++) {
            state[i] = (uint8_t)i;
        }
    }
    memcpy(ref, state, len);
    reference(ref);
    if (memcmp(ref, expected, len) != 0) {
        fprintf(stderr, "%s test reference is inconsistent\nreference: ", name);
        print_hex(ref, len);
        fprintf(stderr, "expected:  ");
        print_hex(expected, len);
        return -1;
    }
    permute(state);
    if (memcmp(state, expected, len) != 0) {
        fprintf(stderr, "%s failed\ngot:      ", name);
        print_hex(state, len);
        fprintf(stderr, "expected: ");
        print_hex(expected, len);
        return -1;
    }
    return 0;
}

static uint64_t random64(void)
{
    static uint64_t s = UINT64_C(0x6a09e667f3bcc909);

    s ^= s << 13;
    s ^= s >> 7;
    s ^= s << 17;
    return s;
}

static int check_random_one(
    const char* name, size_t len, permute_fn permute, permute_fn reference, size_t trial)
{
    uint8_t got[64];
    uint8_t expected[64];
    size_t i;

    for (i = 0; i < len; i += 8) {
        const uint64_t r = random64();
        memcpy(got + i, &r, 8);
    }
    memcpy(expected, got, len);
    permute(got);
    reference(expected);
    if (memcmp(got, expected, len) != 0) {
        fprintf(stderr, "%s reference mismatch at trial %zu\n", name, trial);
        return -1;
    }
    return 0;
}

static int check_random(void)
{
    size_t trial;

    for (trial = 0; trial < 1000; trial++) {
        if (check_random_one("AREION-256", 32, areion256_permute, areion256_reference, trial) != 0
            || check_random_one("AREION-512", 64, areion512_permute, areion512_reference, trial)
                != 0) {
            return -1;
        }
    }
    return 0;
}

